double _r(String t) {
  var h = 0;
  for (final c in t.codeUnits) {
    h = (h * 31 + c) & 0x7fffffff;
  }
  return 3.8 + (h % 12) / 10.0;
}

LibraryItem _f(int i, String title, int year, String topic) => LibraryItem(
  id: "f-$i",
  category: LibraryCategory.film,
  title: title,
  author: "",
  summary: topic,
  genre: topic,
  topic: topic,
  year: year,
  rating: double.parse(_r(title).toStringAsFixed(1)),
  ratingMax: 5,
  coverUrl: kCoverUrls["f-$i"],
);

